void main() {
  var grade = 'A';

  switch (grade) {
    case 'A':
      {
        print("Execellent");
      }
      break;

    case 'B':
      {
        print("Good");
      }
      break;
    case 'C':
      {
        print("Bad");
      }
      break;
    case 'F':
      {
        print("Fail");
      }
      break;
    default:
      {
        print("Wrong input");
      }
  }
}
